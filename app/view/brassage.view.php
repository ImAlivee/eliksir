                <li> 
                    <div class="gauche">
                        <img src="public/images/etapes_brassage/Le_Maltage_web.jpg" alt="maltage" id="maltage">
                    </div>
                    <div>
                        <h2>Maltage</h2>
                        <p>Le maltage est le processus de transformation des céréales en malt. Pendant le maltage, les céréales sont mises à germer, ce qui entraîne la production de glucose et d’enzymes nécessaires à la fabrication de l’alcool lors de la fermentation. Le maltage est une étape importante dans la production de bière, car le malt est utilisé comme ingrédient clé pour fournir des sucres fermentescibles et des arômes spécifiques. </p>
                    </div>
                </li>
                <li> 
                    <div>
                        <h2>Brassage</h2>
                        <p>Lors du brassage de la bière, le malt est concassé pour libérer les sucres et les enzymes nécessaires à la fermentation. Durant cette étape, on ajoute les différents malts afin d’ajouter les saveurs . Le malt concassé sera ensuite mélangé avec de l'eau chaude lors de l'étape de l'empâtage.</p>
                    </div>
                    <div class="droite">
                        <img src="public/images/etapes_brassage/Le_Brassage_web.jpg" alt="brassage" id="brassage">
                    </div>
                </li>
                <li> <div class="gauche">
                        <img src="public/images/etapes_brassage/Lempatage_web.jpg" alt="empatage" id="empatage">
                    </div>
                    <div>
                        <h2>Empatage</h2>
                        <p>L’étape de l’empatage consiste à tremper le malt concassé dans de l'eau chaude afin d'extraire les sucres fermentescibles et les arômes du malt. Cela permet d’activer les enzymes.</p>
                    </div>
                </li>
                <li> 
                    <div>
                        <h2>Filtration</h2>
                        <p>La filtration de la bière est une étape cruciale du processus de brassage qui permet de clarifier et de purifier la bière en éliminant les particules solides indésirables. En filtrant les parties solides, cela permet d’obtenir le moût clarifié.</p>
                    </div>
                    <div class="droite">
                        <img src="public/images/etapes_brassage/La_Filtration_web.jpg" alt="filtration" id="filtration">
                    </div>
                </li>
                <li> <div class="gauche">
                        <img src="public/images/etapes_brassage/Ebullition_web.jpg" alt="ébulition" id="ebulition">
                    </div>
                    <div>
                        <h2>Ébulition</h2>
                        <p>L'ébullition dans le processus de brassage de la bière est une étape essentielle au cours de laquelle le moût est porté à ébullition pendant 60 à 90 minutes. Pendant cette phase, les houblons sont ajoutés pour apporter l'amertume, les arômes et les saveurs caractéristiques à la bière. Cette étape permet également la stérilisation du moût. Une fois l'ébullition terminée, le moût est refroidi rapidement pour passer à la prochaine étape du processus de brassage.</p>
                    </div>
                </li>
                <li> 
                    <div>
                        <h2>Refroidissement</h2>
                        <p>Le refroidissement du moût dans le processus de brassage de la bière est une étape critique qui suit l'ébullition. Après la fin de l'ébullition, le moût doit être transféré et refroidi rapidement pour éviter les contaminations et favoriser la fermentation. Le contrôle précis de la température est essentiel pour garantir un environnement propice à l'action de la levure qui transformera les sucres en alcool et en dioxyde de carbone. .</p>
                    </div>
                    <div class="droite">
                        <img src="public/images/etapes_brassage/Refroidissement_web.jpg" alt="refroidissement" id="refroidissement">
                    </div>
                </li>
                <li> <div class="gauche">
                        <img src="public/images/etapes_brassage/Fermentation_web.jpg" alt="fermentation" id="fermentation">
                    </div>
                    <div>
                        <h2>Fermentation</h2>
                        <p>La fermentation consiste en la transformation des sucres présents dans le moût en alcool et en dioxyde de carbone par l'action des levures. Une fois que le moût refroidi est prêt, il est ensemencé avec une quantité spécifique de levure. Les levures commencent à se multiplier de manière exponentielle et à consommer les sucres présents dans le moût. Cela produit de l'alcool et du dioxyde de carbone. </p>
                    </div>
                </div>
                </li>
                <li> 
                    <div>
                        <h2>Maturation</h2>
                        <p>Après la fermentation, la bière est transférée dans des cuves de maturation où elle repose à une température contrôlée pour développer ses saveurs, se clarifier et se stabiliser. Pendant cette période, la bière subit une clarification naturelle, une éventuelle carbonatation et des contrôles de qualité réguliers.</p>
                    </div>
                    <div class="droite">
                        <img src="public/images/etapes_brassage/Maturation_web.jpg" alt="maturation" id="maturation">
                    </div>
                </li>
            <li> 
                <div class="gauche">
                    <img src="public/images/etapes_brassage/Lembouteillage_web.jpg" alt="embouteillage" id="embouteillage">
                </div>
                <div>
                    <h2>Embouteillage</h2>
                    <p>L'embouteillage de la bière est la dernière étape du processus de brassage, impliquant le lavage des bouteilles, l'ajout de sucre pour la refermentation, le remplissage précis des bouteilles, la capsulation hermétique, l'étiquetage et le conditionnement. Ces étapes garantissent la qualité, la fraîcheur et la sécurité de la bière avant qu'elle ne soit prête à être distribuée et dégustée par les consommateurs</p>
                </div>
                </li>
            </ul>
